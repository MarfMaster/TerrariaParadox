using Terraria;
using Terraria.ID;
using Terraria.ModLoader;
using TerrariaParadox.Content.Items.Materials;
using TerrariaParadox.Content.Items.Tiles.Bars;
using TerrariaParadox.Content.Items.Tiles.Blocks;

namespace TerrariaParadox.Content.Items;

public class VanillaItemRecipes : GlobalItem
{
    public override void AddRecipes()
    {
        Recipe Magiluminescence = Recipe.Create(ItemID.Magiluminescence)
            .AddIngredient(ModContent.ItemType<ChitiniteBar>(), 12)
            .AddIngredient(ItemID.Topaz, 5)
            .AddTile(TileID.Anvils);
        Magiluminescence.Register();
        
        Recipe ShadowCandle = Recipe.Create(ItemID.ShadowCandle)
            .AddIngredient(ModContent.ItemType<ChitiniteBar>(), 3)
            .AddIngredient(ItemID.Torch)
            .AddTile(TileID.WorkBenches);
        ShadowCandle.Register();
        
        Recipe VoidBag = Recipe.Create(ItemID.VoidLens)
            .AddIngredient(ItemID.Bone, 30)
            .AddIngredient(ItemID.JungleSpores, 15)
            .AddIngredient(ModContent.ItemType<BioluminescentGoop>(), 30)
            .AddTile(TileID.DemonAltar);
        VoidBag.Register();
        
        Recipe VoidVault = Recipe.Create(ItemID.VoidVault)
            .AddIngredient(ItemID.Bone, 15)
            .AddIngredient(ItemID.JungleSpores, 8)
            .AddIngredient(ModContent.ItemType<BioluminescentGoop>(), 15)
            .AddTile(TileID.DemonAltar);
        VoidVault.Register();
        
        Recipe ObsidianHelm = Recipe.Create(ItemID.ObsidianHelm)
            .AddIngredient(ItemID.Silk, 10)
            .AddIngredient(ItemID.Obsidian, 20)
            .AddIngredient(ModContent.ItemType<BioluminescentGoop>(), 5)
            .AddTile(TileID.Hellforge);
        ObsidianHelm.Register();
        
        Recipe ObsidianChestplate = Recipe.Create(ItemID.ObsidianShirt)
            .AddIngredient(ItemID.Silk, 10)
            .AddIngredient(ItemID.Obsidian, 20)
            .AddIngredient(ModContent.ItemType<BioluminescentGoop>(), 10)
            .AddTile(TileID.Hellforge);
        ObsidianChestplate.Register();
        
        Recipe ObsidianPants = Recipe.Create(ItemID.ObsidianPants)
            .AddIngredient(ItemID.Silk, 10)
            .AddIngredient(ItemID.Obsidian, 20)
            .AddIngredient(ModContent.ItemType<BioluminescentGoop>(), 5)
            .AddTile(TileID.Hellforge);
        ObsidianPants.Register();
    }
}